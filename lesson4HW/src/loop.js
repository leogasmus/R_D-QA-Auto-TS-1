// Вивід від 0 до 10 через while
let i = 0;
while (i < 10) {
    console.log(i);
    i++;
}

// Вивід від 0 до 10 через do while
let j = 0;
do {
    console.log(j);
    j++;
} while (j < 10);

// Вивід від 0 до 10 через for
for (let i = 0; i < 10; i++) {
    console.log(i);
}

// Вивід від 100 до 0 через while
let k = 100;
while (k >= 0) {
    console.log(k);
    k -= 10;
}

// Вивід від 100 до 0 через do while
let l = 100;
do {
    console.log(l);
    l -= 10;
} while (l >= 0);

// Вивід від 100 до 0 через for
for (let i = 100; i >= 0; i -= 10) {
    console.log(i);
}
